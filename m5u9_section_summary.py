#Inheritance allows us to reuse existing code 
#Child classes can use existing code from parent class
#Child classes can be used to add or change functionality without affecting parent class
#Parent class is more general e.g. Bank Account / Child class is more specialised e.g. Checking/Savings Account
#Importing classes can be done using the import function e.g. import [file name(no extension)]
#Importing classes allow us to separate code to make it more manageable and structured
#Overriding methods are when a child class provides its own version of a method that already exists in the parent class
#Can always call parent methods within child classes
